		<h4>Abstract</h4>
		<p>Threaded connections are responsible assemblies, defining sucker rod string reliability. Despite the variety of their design, industrial materials analysis shows that breakages occur in rod body and threaded connections.
In the process of lowering and lifting operations, aimed at well pump replacement, screwing and unscrewing of sucker rods threaded joints is repeated, so pin threads and couplings are worn.
After analyzing the method of pin threads wear determining while screwing, unscrewing, connection and breakout sucker rods tightening, proposed by S. A. Babayan, it has been found out that the amount of thread wear during the makeup is 20–30
times greater than thread wear while fastening. According to this method the last three threads are practically unworn and deterioration of the first coil is 10–12 times greater than the wear of the last turns. The results of calculation correspond with the
wear data in the course of sucker rods operation in an industrial environment. To identify and compare the distribution of stresses in the sucker rods threaded connections using the developed computer
ANSYS, finite element axisymmetric models of new and worn standard threaded connections with a diameter of 19 mm have been created. Screw connections were loaded by axial force and torque make-up.</p>
		<p>Having analyzed the simulation results we have made a conclusion that with the increase of sucker rod wear, stress distribution in pin thread becomes uniform, respectively, it has positive effect on the threaded connection durability under cyclic
dynamic loads. With further deterioration, thread tensions are rising in the middle turns, and in extreme turns – are reduced, so the impact of further deterioration on the connection durability is negative.
Also it have been found that at the limit thread wear maximum stresses are concentrated in the thread coupling, resulting in the fracture of its body.</p>
