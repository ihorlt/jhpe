		<h4>Abstract</h4>
		<p>There are described the main aspects of protection of underground oil-and-gas pipelines against microbial corrosion by soil corrosion-hazardous microorganisms, including the crucial role played by sulfate-reducing (SRB) and thione (TB) bacteria. It is
researched the effect of nitrogen-containing organic inhibitors of corrosion on the growth and enzyme activity of the sulfur cycle bacteria and the locking mechanism of TB, it is also determined hydrogenasic reaction of corrosion-active SRB. There is
analyzed the influence of the dioxo-decahydroacridine derivatives on the rate of microbial corrosion of steel under the SRB and TB effect. It is theoretically researched the bioresistance of dioxo-decahydroacridine derivatives. The effectiveness of these
inhibitors and the industrial "J" inhibitor is compared. The researched inhibitors provide with a high degree of protection against corrosion in the presence of SRB (90%), indicating their antibacterial properties and showing the prospect of their use in
industrial conditions of anaerobic corrosion that is caused by SRB.</p>
		<p>There are identified the bacteria associates of four physiological groups of the damaged gas pipelines primers that are laid in the regions I, II and III. Their qualitative and quantitative composition is determined. It is found out that the dominant bacteria
in association of the isolated bacteria are denitrifying (DN) bacteria; this indicates the intensity of denitrification processes in the underground environment. As a result of this both the protective insulation coating and a primer are destroyed.
There are carried out industrial tests of innovative biological stable protective coating on the basis of the modified by inhibitor "J" mastic at the Regional Pipeline Department "Prykarpattransgas".</p>
