		<h4>Abstract</h4>
		<p>The article aims at the development and testing of the methods of gas flow intensification on the Sarmatian sediments
samples of Bilche–Volytske zone. The existing standard methods for petrophysical study of reservoir rocks have served as the
methodological basis for the implementation of research. Studies have been performed on three samples of reservoir rocks by
modeling their contamination with drilling mud, resulting in the reduction of effective phase permeability of gas. The
permeability of reservoir rocks has been restored with the help of modern foaming reagent, hydrochloric and acetic acid.</p>
<p>Lithological and petrographic features of the samples have been considered to determine their role in the restoration of
effective permeability of gas bearing Sarmatian sediments. </p>
