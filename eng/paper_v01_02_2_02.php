		<h4>Abstract</h4>
		<p>Development of unconventional gas requires specific technical and technological means and is accompanied by diverse high risks. The lack of practical experience and therefore reliable data on the conditions of exploration and production requires a comprehensive technical and economic approach to assess risks of development of unconventional resources, including shale in gas Ukraine. Wide experience in production of unconventional gas, mainly shale gas in the U.S., can be extrapolated and quantifies the risks in Ukraine, considering its geological and technological conditions, economic factors and fiscal systems.</p>
		<p>Stochastic simulation by Monte Carlo method showed that the geological part of the risks associated with the volume of gas recoverable reserves were estimated at 22–25%, technical risks associated with the initial production rate of wells – at 2–17%. Financial and economic risks associated with the cost of drilling, caused primarily by their depth, were estimated at 23–25%. Up to 45% of risks are determined by the price of gas. Quantitative risks are ranked for determining the priority areas of improvement the degree of projects commercialization.</p>
