		<h4>Abstract</h4>
		<p>The aim of the study is to calculate the parameters of the movable pipeline ship unloading as a part of the cargo plan of a CNG ship, to develop a technological scheme of the pipeline strapping of the containers system, to develop an unloading scheme
of a CNG ship under the conditions of the different schemes of mooring, to calculate the parameters of the gas flow during the unloading process. The scientific novelty of the work is to substantiate the technological strapping of the container blocks in order to make loading/unloading of the CNG vehicle uniform, to determine the required performance of unloading points and to ensure the
unloading duration up to 12 hours. According to the research there is offered the unloading scheme of the CNG vehicle under the conditions of the bow mooring to the port facilities and the presence of the bow and stern containers blocks connections to the pipeline
communications</p>
		<p>There are offered two concurrent unloading points with the maximum unloading capacity of 150 kg/s. The further layout of the turbo expander and compressor equipment should result from the offered parameters scheme.
It is proved that the offered pipeline communication scheme provides with the uniform ship unloading (pressure difference in individual containers blocks (0,5–0,7 bar), however, there is a significant uniform (up to -30°С) temperature lowering of the
gas due to physical processes of the gas throttling in CNG cylinders. During further research studies it should be considered the impact of the temperature lowering on the mechanical properties of the CNG cylinders composite material. Generally, the offered
unloading scheme will allow ship unloading in the economically reasonable period of 12 hours. The results should be used for further development of the technological scheme of ground equipment for connecting of a
CNG vehicle, for choosing the technological equipment, assessing the gas flow parameters influence on the mechanical properties of the pipes metal and the composite material.</p>
