		<h4>Abstract</h4>
		<p>With the use of ideas of quasiconformal mapping and procedure of gradual recording of medium and process characteristic
        the methodology of mathematical modeling of filtration and mass-exchanging processes in well bore zone, upon conditions of
        occurrence of gas formation hydraulic fractures has been suggested. The influence of thermodynamic effects, caused by gas
        throttling through microfractures has been taken into consideration, when filtration process is described by specially modified
        Darcy’s law with critical pressure gradient. Numerical algorithm to solve correspondent boundary tasks, build flow pattern,
        temperature field, filtration properties etc. has been developed.</p>
