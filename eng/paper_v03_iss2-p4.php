		<h4>Abstract</h4>
		<p>The research is made to identify the places of intense strikes of liquid and solid particles to the wall of compressor station
manifold of a gas pipeline, their erosive wear and to calculate the erosion rate.</p>
        <p>There is carried out 3D modeling of compressor station piping and its shaped elements, where a complex movement of
multiphase flows, change of their direction, swirl, strikes of discrete phases to the wall of a pipeline as well as erosive wear of the
pipeline wall occur.</p>
        <p>Based on the Lagrangian approach (the Discrete Phase Model) there were developed methods for modeling the erosive
wear of compressor station manifold shaped elements (bends, T-junctions) using ANSYS Fluent R17.0 Academic software. A
mathematical model is based on solving the Navier–Stokes, continuity, discrete phases motion and the Finney equations, two–
parameter k −ε Launder–Sharma turbulence model with appropriate initial and boundary conditions. The simulation was
performed for different motion patterns of gas (gas moves through T-junction run-pipe to the T-junction branch; gas moves
through the branch of T-junction to the T-junction run-pipe, in which a portion of gas stream flows in one side of the run-pipe,
and the rest of the gas stream – in the other one; gas moves through the T-junction branch to one side of the T-junction run-pipe).</p>
        <p>The simulation results were visualized in ANSYS Fluent R17.0 Academic postprocessor by building concentration fields of
a discrete phase and erosion rate fields at shaped elements contours. Having studied the obtained results there were identified
places of intense strikes of liquid and solid particles to the wall of shaped elements of compressor station manifold, the intensive
erosive wear of a pipeline wall and there was calculated the erosion rate.</p>
