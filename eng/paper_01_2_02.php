		<h4>Abstract</h4>
		<p>Analytical methods were used for engineering design of oil and gas fields in olden times (old fields). Nowadays numerical methods based on computer geological and mathematical models (new fields) have leading positions. While using numerical
methods it is necessary to undergo a stage of history match to real condition with a switch to a live simulation model. With analytical methods a great part of information was not received, and there was no need in it. Nowadays it is impossible to build a
computer geological and mathematical model and it is not of economic benefit. But in both cases concerning new fields as well as the old ones there is a need of additional study of the fields at the final stage of the development and assessment of reservoir
behavior in future on the basis of development history statistical data.</p>
<p>Well rate (oil wells, gas wells, injection wells, water wells) changes with the pace of time due to different reasons, particularly due to reservoir conditions change, bottomhole zone damage as well as decrease of production equipment efficiency.
This paper presents a universal law of well rate decrease using statistical laws of Makeham and Weilbull and united Weilbull–Makeham law according to which well rate decrease is characterized by four parameters which makes better description of well
behavior possible. The deduced law is based on the law of probability. This law provides for getting logarithmic, harmonic, hyperbolic and parabolic laws, and in some cases several other laws can be deduced. The examples of law application are
provided.</p>
