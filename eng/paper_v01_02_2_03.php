		<p>The deformation of a drill string elastic line with external diameter of 127 mm in a well “dog-leg” was analyzed in real operational conditions using the developed method. The author obtained the function of a drill string elastic line in 3D coordinate system. There were drawn the diagrams of normal stresses of bending around basic axes of inertia of a drill string cross-section. It is also noticed that the “dog-leg” increases these stresses twice.</p>
