		<h4>Abstract</h4>
		<p>The most topical task is further development of testing methods of oil and gas industry objects by ultrasonic guided waves.
The aim of research is to develop mathematical model of ultrasonic guided waves propagation in oil and gas pipelines made
from steel.The method of research is a computer modelling of ultrasonic guided waves propagation in waveguides with cross-sections
of finite size.</p>
		<p>Calculations of guided waves propagation have been performed in two spectral ranges. With the increase of frequency an
algorithm finds the same number of modes in a more narrow frequency spectra. Numerical results of found modes were
estimated by effective mass criterion. The criterion shows that only a few modes from the found set correspond with applied
boundary conditions of ultrasonic guided waves propagation. Results of calculations are applied to ultrasonic guided waves propagation along the V weld. Welded joints sizes meet
requirements of normative documents about oil and gas main pipelines.</p>
		<p>In the paper it is specified that the parameters of ultrasonic guided waves propagation in a waveguide with its cross-cuts of
finite sizes can be calculated utilizing existent algorithms realising search of eigenvalues, based on Timoshenko beam which is a
finite element typeIt has been found out that not all results of eigenvalues calculation by the algorithm with boundary conditions
that describes propagation of guided waves with specified wavenumber correspond to propagation of the modes. The results of
calculations have to be filtered out by the criterion of effective mass. It has been shown that the criterion of effective mass of a
mode can be used to determine the type of guided wave. It has been found out that modes, propagating in a waveguide with its
cross-cuts of finite sizes, can have marked torsional displacements without additional axial movements.</p>
