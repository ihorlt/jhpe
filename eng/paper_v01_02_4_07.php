		<h4>Abstract</h4>
		<p>Existing pipeline networks used for transportation of oil and gas are being exposed for operation for decades resulting in
serious material degradation process occurs in such cases. In this research results of experimental investigation aimed at
determination of the electrical resistivity of structural steels used in gas transmission pipelines with the help of the developed
experimental unit that implements the four-point method was studied. Multi-parameter approach was utilized in the study while
neural networks were used for non-linear approximation of yield strength of pipelines as a function of hardness and electrical
resistivity. Samples with special heat-treatment for microstructure distinguishing as well as a number of samples taken from the
long-term used pipelines were selected. Destructive tensile testing was performed for all samples under investigation and results
were used as references in the study. It was shown that the four-point method can be used to overall metal structures, since the
measured value of electrical resistivity does not affect the whole width of the object of control, but only so-called conditional
effective width. Under the conditional effective width of the sample should be understood that part of the sample, in which the
density of direct current passing through the object, is the largest and which actually affects the measured value of electrical
resistivity. Combined measurement of the hardness together with electrical resistivity after neural network processing showed to
achieve 26 MPa accuracy for yield strength determination at real-life pipelines.</p>
