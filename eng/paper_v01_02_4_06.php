		<h4>Abstract</h4>
		<p>The analysis of the relative uncertainty of the measurement result equation of gas flowrate, using differential pressure
flowmeters with standard orifice plates, defined in GOST 8.586.5-2005 and DSTU GOST 8.586.5:2009, is presented. It has been
found out that this equation is presented in a simplified form by introducing simplified equations for calculation of relative
influence coefficients of input variables upon mass gas flow rate and relative uncertainty of gas expansion factor. New
analytical dependencies for estimation of relative coefficients of influence of differential pressure on orificeplate, of orificeplate
hole diameter and measuring pipeline inner diameter on mass gas flow rate. The equation, used to calculate relative uncertainty
of gas.expansion factor has been specified. The new equations, used to determine relative influence coefficients of absolute
pressure and gas isentropic exponent coefficient on gas expansion factor have been obtained. It has been found out that relative
influence coefficients of input variables on mass flow rate of gas and gas expansion factor accept lower values than coefficients
of equations, given in Standards. Values of relative influence coefficients obtained from the authors' equations are in good
agreement with the results of the calculation of the equations presented in differential form and given in ISO 5168:2005. The
analytical dependence reduces relative uncertainty of gas flow rate measurement result.</p>
